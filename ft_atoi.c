#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		neg;
	int		nbr;

	i = 0;
	neg = 1;
	nbr = 0;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while ((str[i] >= '0' && str[i] <= '9') && str[i] != '\0')
	{
		nbr = (nbr * 10) + str[i++] - '0';
	}
	return (neg * nbr);
}
